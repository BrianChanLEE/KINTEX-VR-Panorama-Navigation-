import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const META_PATH = "/Users/youngchanlee/Downloads/kmice-vr/public/panos/meta.json";
const EXTRACTED_PATH = "/Users/youngchanlee/Downloads/kmice-vr/extracted_scenes.json";
const TARGET_SCENES_PATH = "/Users/youngchanlee/Downloads/kmice-vr/src/data/scenes.ts";
const TARGET_FACILITY_PATH = "/Users/youngchanlee/Downloads/kmice-vr/src/data/facility.ts";

const translateText = (text) => {
  if (!text) return "";
  let en = text;
  en = en.replace(/항공(\d+)/g, "Aerial View $1");
  en = en.replace(/전시(\d+)홀/g, "Exhibition Hall $1");
  en = en.replace(/전시(\d+)홀A\+B\+C 강의식/g, "Exhibition Hall $1 A+B+C (Lecture)");
  en = en.replace(/전시(\d+),(\d+)홀 로비/g, "Hall $1-$2 Lobby");
  en = en.replace(/전시(\d+)홀 로비/g, "Hall $1 Lobby");
  en = en.replace(/전시(\d+)A,(\d+)B 로비/g, "Hall $1A-$2B Lobby");
  en = en.replace(/(\d+)홀 입구/g, "Hall $1 Entrance");
  en = en.replace(/화상상담실 입구/g, "Video Conferencing Room Entrance");
  en = en.replace(/화상상담실 로비/g, "Video Conferencing Room Lobby");
  en = en.replace(/화상상담실(\d+)/g, "Video Conferencing Room $1");
  en = en.replace(/그랜드볼룸A\+B/g, "Grand Ballroom A+B");
  en = en.replace(/그랜드볼룸([A-Z])/g, "Grand Ballroom $1");
  en = en.replace(/무빙워크/g, "Moving Walkway");
  en = en.replace(/(\d+)F로비/g, "$1F Lobby");
  en = en.replace(/(\d+)F 로비/g, "$1F Lobby");
  en = en.replace(/(\d+)F복도/g, "$1F Hallway");
  en = en.replace(/(\d+)~(\d+)복도/g, "$1-$2 Hallway");
  en = en.replace(/(\d+)~(\d+) 복도/g, "$1-$2 Hallway");
  en = en.replace(/(\d+)입구/g, "Room $1 Entrance");
  
  if (en === text) {
    if (/^\d+$/.test(text)) return `Room ${text}`;
    if (/^\d+[A-Z]$/.test(text)) return `Room ${text}`;
    if (text.includes("VIP회의실")) {
      const num = text.replace(/[^0-9A-Z]/g, "");
      return `${num} (VIP Conference Room)`;
    }
  }
  return en;
};

async function run() {
  const meta = JSON.parse(await readFile(META_PATH, "utf8"));
  const extracted = JSON.parse(await readFile(EXTRACTED_PATH, "utf8"));
  const keyToZoneMap = Object.fromEntries(extracted.map((item) => [item.key, item.zone]));
  
  // 1. Generate scenes.ts
  const scenes = meta.map((s) => {
    const titleKo = s.title;
    const titleEn = translateText(s.title);
    const resolvedZone = keyToZoneMap[s.key] || s.zone;
    
    const hotspots = s.hotspots.map((h, idx) => {
      const label = h.name || "";
      const labelEn = translateText(label);
      return {
        id: `${s.key}-h${idx + 1}`,
        lon: Number(h.lon.toFixed(2)),
        lat: Number(h.lat.toFixed(2)),
        label,
        labelEn,
        kind: h.kind,
        target: h.target,
        url: h.url || undefined,
        sub: h.desc || undefined
      };
    });

    return {
      id: s.key,
      ko: titleKo,
      en: titleEn,
      index: s.key.match(/\d+/) ? s.key.match(/\d+/)[0] : "01",
      zone: resolvedZone,
      img: `/panos/${s.key}.jpg`,
      startLon: Number(s.startLon.toFixed(2)),
      startLat: Number(s.startLat.toFixed(2)),
      hotspots
    };
  });

  const scenesCode = `// SIC27-KINTEX 360° VR — scene & hotspot data (AUTOGENERATED)
// Do not modify directly. Run scripts/generate-scenes-data.mjs instead.

export type ZoneId =
  | "aerial"
  | "exterior"
  | "floor1_1"
  | "floor1_2"
  | "floor1_3"
  | "movingwalk"
  | "floor2_l"
  | "floor2_h"
  | "floor2_3"
  | "floor2_4";

export interface Zone {
  id: ZoneId;
  ko: string;
  code: string;
  en: string;
}

export const ZONES: Zone[] = [
  { id: "aerial", ko: "항공", code: "AIR", en: "Aerial" },
  { id: "exterior", ko: "야외", code: "OUT", en: "Outdoor" },
  { id: "floor1_1", ko: "1전시장 1F", code: "1-1F", en: "Center 1 - 1F" },
  { id: "floor1_2", ko: "1전시장 2F", code: "1-2F", en: "Center 1 - 2F" },
  { id: "floor1_3", ko: "1전시장 3F", code: "1-3F", en: "Center 1 - 3F" },
  { id: "movingwalk", ko: "무빙워크", code: "MW", en: "Moving Walkway" },
  { id: "floor2_l", ko: "2전시장 로비", code: "2-LBY", en: "Center 2 - Lobby" },
  { id: "floor2_h", ko: "2전시장 전시홀", code: "2-HALL", en: "Center 2 - Hall" },
  { id: "floor2_3", ko: "2전시장 3F", code: "2-3F", en: "Center 2 - 3F" },
  { id: "floor2_4", ko: "2전시장 4F", code: "2-4F", en: "Center 2 - 4F" },
];

export type HotspotKind = "nav" | "poi" | "info";

export interface Hotspot {
  id: string;
  lon: number;
  lat: number;
  label: string;
  labelEn: string;
  sub?: string;
  kind: HotspotKind;
  target?: string;
  url?: string;
}

export interface Scene {
  id: string;
  ko: string;
  en: string;
  index: string;
  zone: ZoneId;
  img: string;
  startLon: number;
  startLat: number;
  hotspots: Hotspot[];
}

export const SCENES: Scene[] = ${JSON.stringify(scenes, null, 2)};

export const getScene = (id: string): Scene =>
  SCENES.find((s) => s.id === id) ?? SCENES[0];

export const scenesByZone = (zone: ZoneId): Scene[] =>
  SCENES.filter((s) => s.zone === zone);
`;

  await writeFile(TARGET_SCENES_PATH, scenesCode, "utf8");

  // 2. Generate facility.ts
  const facilityData = {};
  const safetyCommon = [
    { label: "비상구", value: "8개소 (양방향)" },
    { label: "소화전 · 소화기", value: "구역별 배치" },
    { label: "AED 심장충격기", value: "로비 1층 비치" },
    { label: "제연 설비", value: "자동 급·배기" },
    { label: "안전 요원", value: "상시 순찰" },
  ];

  meta.forEach((s) => {
    const desc = s.desc || `${s.title} 전경입니다.`;
    const general = [];
    const space = [];

    // Parse generalInfo if available
    if (s.generalInfo && Array.isArray(s.generalInfo.bodyItems)) {
      s.generalInfo.bodyItems.forEach((item) => {
        const row = { label: item.name, value: item.value.replace(/\n/g, " ") };
        if (["규모", "천고", "바닥하중", "수직 동선", "층고", "부스 (3×3)", "전시면적"].includes(item.name)) {
          space.push(row);
        } else {
          general.push(row);
        }
      });
    }

    if (general.length === 0) {
      general.push({ label: "명칭", value: s.title });
      general.push({ label: "위치", value: s.zone === "aerial" ? "상공 조망" : "킨텍스 구역 내" });
    }
    if (space.length === 0) {
      space.push({ label: "공간 구분", value: s.zone === "hall" ? "전시 전용홀" : "공용 이동 동선" });
    }

    facilityData[s.key] = {
      desc,
      general,
      safety: safetyCommon,
      space
    };
  });

  const facilityCode = `// Facility detail content shown in the 시설 상세정보 panel (AUTOGENERATED).
// Do not modify directly. Run scripts/generate-scenes-data.mjs instead.

export type InfoTab = "general" | "safety" | "space";

export interface InfoRow {
  label: string;
  value: string;
}

export interface FacilityInfo {
  general: InfoRow[];
  safety: InfoRow[];
  space: InfoRow[];
  desc: string;
}

export const FACILITY: Record<string, FacilityInfo> = ${JSON.stringify(facilityData, null, 2)};

export const getFacility = (id: string): FacilityInfo =>
  FACILITY[id] ?? FACILITY[Object.keys(FACILITY)[0]];
`;

  await writeFile(TARGET_FACILITY_PATH, facilityCode, "utf8");

  console.log("SCENES & FACILITY data updated successfully.");
}

run().catch(console.error);
